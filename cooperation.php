        function getData(obj_form) {
            var hData = {};
            $('input, select', obj_form).each(function() {
                if (this.name && this.name != '') {
                    if ($(this).is(':checked')) {
                        this.value = "yes";
                    }
                    hData[this.name] = this.value;
                    console.log("hData: " + this.name + " : " + this.value);
                }
            });
            return hData;
        }

        function submit() {
            var postData = getData('.questionnaire__form');
            $.ajax({
                dataType: 'json',
                url: "questionnaire.php",
                method: 'POST',
                async: true,
                data: postData,
                success: function(data) {
                    $('.questionnaire__answer .questionnaire__form').html(data['message'])
                    $('.questionnaire__answer .questionnaire__form').append(`<svg width="51" height="23" viewBox="0 0 51 23" fill="none" xmlns="http://www.w3.org/2000/svg">
                                                                            <path d="M0 10H49.3279L50.5 11L49.3279 12H0V10Z" fill="#313131"/>
                                                                            <rect x="38" y="20.6064" width="15" height="2" transform="rotate(-45 38 20.6064)" fill="#313131"/>
                                                                            <rect x="39.4142" width="15" height="2" transform="rotate(45 39.4142 0)" fill="#313131"/>
                                                                            </svg>`)
                    $('.questionnaire__answer').css('left', '0');
                    if (data['success']) {
                        $(".questionnaire").toggleClass("active__form");
                        $(".questionnaire__answer").click(function() {
                            $("body").toggleClass("locked");
                        });
                    }
                }
            });
        };
